        /// <summary>
        /// Returns an array of the rows of the rendered string 
        /// together.  Each character has one pixel (space) between
        /// each letter, and missing characters in the font is
        /// filled with spaces.
        /// </summary>
        public static string[] GetString(string s)
        {
            int lineCount = 5;

            StringBuilder[] sb = new StringBuilder[lineCount];
            for (var i = 0; i < lineCount; i++)
            {
                sb[i] = new StringBuilder();
            }

            foreach (var c in s.ToUpper().ToCharArray())
            {
                var letter = GetChar(c);
                for (var i = 0; i < lineCount; i++)
                {
                    sb[i].Append(letter[i]);
                    sb[i].Append(" ");
                }
            }

            var ret = new string[lineCount];
            for (var i = 0; i < lineCount; i++)
            {
                ret[i] = sb[i].ToString();
            }
            return ret;
        }

